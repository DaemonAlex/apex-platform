// xAPI status responses keyed by deviceId
function buildXapiStatus(device) {
  const uptime = Math.floor(Math.random() * 30 * 86400) + 86400; // 1-30 days in seconds
  return {
    DeviceId: device.id,
    SystemUnit: {
      Hardware: { Module: { SerialNumber: device.serial } },
      Software: { Version: device.firmware, Name: 'RoomOS', ReleaseDate: '2024-11-15' },
      Uptime: uptime,
      State: { System: device.connectionStatus === 'connected' ? 'Initialized' : 'Error' },
    },
    Network: [
      {
        id: '1',
        IPv4: { Address: device.ip, SubnetMask: '255.255.255.0', Gateway: device.ip.replace(/\.\d+$/, '.1') },
        DNS: { Domain: { Name: 'corp.example.com' } },
        Ethernet: { MacAddress: device.mac, Speed: '1000full' },
      },
    ],
    Audio: { Volume: 50 },
    Standby: { State: 'Standby', Delay: 10 },
    Call: [],
    Cameras: { Camera: device.type !== 'roomnavigator' ? [{ id: '1', Connected: 'True', Model: device.product }] : [] },
    Peripherals: {
      ConnectedDevice: device.type !== 'roomnavigator'
        ? [{ id: '1', Name: `${device.displayName} - Navigator`, Type: 'TouchPanel', Status: 'Connected' }]
        : [],
    },
  };
}

// Build the workspace response matching Control Hub API shape
function enrichWorkspace(ws) {
  const loc = LOCATIONS.find(l => l.id === ws.locationId);
  const floor = loc?.floors.find(f => f.id === ws.floorId);
  return {
    id: ws.id,
    orgId: ORG_ID,
    displayName: ws.displayName,
    type: ws.type,
    capacity: ws.capacity,
    locationId: ws.locationId,
    floorId: ws.floorId,
    location: {
      id: ws.locationId,
      displayName: loc?.displayName,
      address: loc?.address,
    },
    floor: floor ? { id: floor.id, displayName: floor.displayName, floorNumber: floor.floorNumber } : null,
    calling: {
      type: 'webexCalling',
      webexCalling: { extension: ws.extension, locationId: ws.locationId },
    },
    calendar: { type: ws.capacity > 0 ? 'microsoft' : 'none' },
    sipAddress: `${ws.extension}@corp.example.com`,
    notes: null,
    created: '2024-01-15T08:00:00.000Z',
  };
}

// Build the device response matching Control Hub API shape
function enrichDevice(dev) {
  const ws = WORKSPACES.find(w => w.id === dev.workspaceId);
  const loc = LOCATIONS.find(l => l.id === dev.locationId);
  return {
    id: dev.id,
    displayName: dev.displayName,
    orgId: ORG_ID,
    workspaceId: dev.workspaceId,
    locationId: dev.locationId,
    product: dev.product,
    productId: dev.productId,
    type: dev.type,
    serial: dev.serial,
    mac: dev.mac,
    ip: dev.ip,
    activeInterface: 'Ethernet',
    software: { releaseChannel: 'stable', version: dev.firmware },
    firmware: dev.firmware,
    upgradeChannel: 'stable',
    connectionStatus: dev.connectionStatus,
    errorCodes: dev.connectionStatus === 'connected_with_issues' ? ['CONFIG_MISMATCH'] : [],
    capabilities: { configured: [] },
    permissions: { configured: [] },
    tags: dev.tags,
    workspaceName: ws?.displayName || null,
    locationName: loc?.displayName || null,
    created: '2024-01-15T08:00:00.000Z',
    lastSeen: new Date(Date.now() - Math.floor(Math.random() * 3600000)).toISOString(),
  };
}

