// Helper to look up location subnet for IP generation
const LOCATION_SUBNET = {};
for (const loc of LOCATIONS) {
  LOCATION_SUBNET[loc.id] = loc.subnet;
}

function ipForWorkspace(wsId, offset) {
  const ws = WORKSPACES.find(w => w.id === wsId);
  const subnet = LOCATION_SUBNET[ws?.locationId] || '10.99';
  return `${subnet}.${Math.floor(offset / 254) + 1}.${(offset % 254) + 2}`;
}

function macFromSerial(serial) {
  const hex = Buffer.from(serial).toString('hex').slice(0, 12).toUpperCase();
  return hex.match(/.{2}/g).join(':');
}

